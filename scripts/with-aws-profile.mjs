/**
 * PartyKit's worker isolate does not inherit arbitrary shell env.
 * It loads `.env` / `.env.local` into process.env (and room.env).
 *
 * This Node wrapper resolves AWS_PROFILE (or the default chain) to temporary
 * keys, writes them to gitignored `.env.local`, then starts PartyKit.
 *
 * Usage:
 *   node scripts/with-aws-profile.mjs partykit dev
 *   AWS_PROFILE=sprint-bedrock npm run partykit:dev
 */
import { spawn } from 'node:child_process';
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { delimiter, dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { fromIni, fromNodeProviderChain } from '@aws-sdk/credential-providers';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const ENV_LOCAL = join(root, '.env.local');

function loadDotEnvFile(envPath) {
  if (!existsSync(envPath)) return;

  for (const raw of readFileSync(envPath, 'utf8').split('\n')) {
    const line = raw.trim();
    if (!line || line.startsWith('#')) continue;
    const eq = line.indexOf('=');
    if (eq <= 0) continue;
    const key = line.slice(0, eq).trim();
    let value = line.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    // Do not override vars already set in the shell (allows one-shot AWS_PROFILE=…)
    if (process.env[key] === undefined) {
      process.env[key] = value;
    }
  }
}

function shellQuote(value) {
  return `'${String(value).replace(/'/g, `'\\''`)}'`;
}

/** PartyKit only sees keys that live in .env / .env.local — not parent process.env. */
function writePartykitEnvLocal() {
  const accessKeyId = process.env.AWS_ACCESS_KEY_ID;
  const secretAccessKey = process.env.AWS_SECRET_ACCESS_KEY;
  if (!accessKeyId || !secretAccessKey) {
    throw new Error('Resolved credentials missing access key or secret');
  }

  const lines = [
    '# Auto-generated by scripts/with-aws-profile.mjs — do not commit.',
    `# Generated at ${new Date().toISOString()}`,
    `AWS_ACCESS_KEY_ID=${shellQuote(accessKeyId)}`,
    `AWS_SECRET_ACCESS_KEY=${shellQuote(secretAccessKey)}`,
  ];

  if (process.env.AWS_SESSION_TOKEN) {
    lines.push(`AWS_SESSION_TOKEN=${shellQuote(process.env.AWS_SESSION_TOKEN)}`);
  }

  if (process.env.AWS_REGION) {
    lines.push(`AWS_REGION=${shellQuote(process.env.AWS_REGION)}`);
  }

  if (process.env.BEDROCK_MODEL_ID) {
    lines.push(`BEDROCK_MODEL_ID=${shellQuote(process.env.BEDROCK_MODEL_ID)}`);
  }

  writeFileSync(ENV_LOCAL, `${lines.join('\n')}\n`, { mode: 0o600 });
  process.stdout.write(`Wrote PartyKit credentials to ${ENV_LOCAL}\n`);
}

loadDotEnvFile(join(root, '.env'));
// Prefer a prior shell/profile resolve over a stale .env.local from last run
loadDotEnvFile(ENV_LOCAL);

const hasStaticKeys =
  Boolean(process.env.AWS_ACCESS_KEY_ID) && Boolean(process.env.AWS_SECRET_ACCESS_KEY);

if (hasStaticKeys && !process.env.AWS_PROFILE) {
  process.stdout.write(
    'Using existing AWS_ACCESS_KEY_ID / AWS_SECRET_ACCESS_KEY (skipping profile resolve).\n'
  );
} else {
  const profile = process.env.AWS_PROFILE;
  process.stdout.write(
    profile
      ? `Resolving AWS credentials from profile "${profile}"…\n`
      : 'Resolving AWS credentials from default provider chain…\n'
  );

  try {
    const provider = profile ? fromIni({ profile }) : fromNodeProviderChain();
    const creds = await provider();
    process.env.AWS_ACCESS_KEY_ID = creds.accessKeyId;
    process.env.AWS_SECRET_ACCESS_KEY = creds.secretAccessKey;
    if (creds.sessionToken) {
      process.env.AWS_SESSION_TOKEN = creds.sessionToken;
    } else {
      delete process.env.AWS_SESSION_TOKEN;
    }
    process.stdout.write('AWS credentials resolved.\n');
  } catch (error) {
    const message = error instanceof Error ? error.message : String(error);
    console.error(`Failed to resolve AWS credentials: ${message}`);
    console.error(
      profile
        ? `Try: aws sso login --profile ${profile}`
        : 'Set AWS_PROFILE in .env or export it, then aws sso login --profile <name>'
    );
    process.exit(1);
  }
}

try {
  writePartykitEnvLocal();
} catch (error) {
  const message = error instanceof Error ? error.message : String(error);
  console.error(`Failed to write .env.local for PartyKit: ${message}`);
  process.exit(1);
}

const args = process.argv.slice(2);
if (args.length === 0) {
  console.error('Usage: node scripts/with-aws-profile.mjs <command> [args…]');
  process.exit(1);
}

const env = {
  ...process.env,
  PATH: `${join(root, 'node_modules', '.bin')}${delimiter}${process.env.PATH ?? ''}`,
};

const child = spawn(args[0], args.slice(1), {
  stdio: 'inherit',
  env,
  cwd: root,
});

child.on('exit', (code, signal) => {
  if (signal) {
    process.kill(process.pid, signal);
    return;
  }
  process.exit(code ?? 1);
});
